import { createSlice } from "@reduxjs/toolkit"

// Create a slice for watch later movies with initial state and reducers
const watchLaterSlice = createSlice({
    name: 'watch-later',
    initialState: {
        watchLaterMovies: [] // Array to store watch later movies
    },
    reducers: {
        // Action to add a movie to the watch later list
        addToWatchLater: (state, action) => {
            state.watchLaterMovies = [action.payload, ...state.watchLaterMovies]
        },
        // Action to remove a movie from the watch later list
        removeFromWatchLater: (state, action) => {
            const indexOfId = state.watchLaterMovies.findIndex(key => key.id === action.payload.id)
            state.watchLaterMovies.splice(indexOfId, 1)
        },
        // Action to clear all movies from the watch later list
        remveAllWatchLater: (state) => {
            state.watchLaterMovies = []
        },
    },
})

// Export the reducer generated by the slice
export default watchLaterSlice
