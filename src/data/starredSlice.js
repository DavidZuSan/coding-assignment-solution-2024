import { createSlice } from "@reduxjs/toolkit";

// Create a slice for starred movies with initial state and reducers
const starredSlice = createSlice({
    name: 'starred',
    initialState: {
        starredMovies: [] // Array to store starred movies
    },
    reducers: {
        // Action to add a movie to the starred list
        starMovie: (state, action) => {
            state.starredMovies = [action.payload, ...state.starredMovies];
        },
        // Action to remove a movie from the starred list
        unstarMovie: (state, action) => {
            const indexOfId = state.starredMovies.findIndex(key => key.id === action.payload.id);
            state.starredMovies.splice(indexOfId, 1);
        },
        // Action to clear all starred movies
        clearAllStarred: (state) => {
            state.starredMovies = [];
        },
    },
});

// Export the reducer generated by the slice
export default starredSlice;
